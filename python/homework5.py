# 4 Суммирование тысячу чисел: создайте список чисел от 1
# до 1 000, затем воспользуйтесь функциями min() и max() и
# убедитесь в том, что список действительно начинается с 1 и
# заканчивается 1 000. Вызовите функцию sum() и
# посмотрите, насколько быстро Python сможет просуммировать
# тысячу чисел

nums = []
for i in range(1,1001):
    nums.append(i)
print(min(nums))
print(max(nums))
print(sum(nums))
print(nums)


