# 6 Создайте бесконечный цикл. Запросите у пользователя его возраст. Если
# ему есть 18 лет, выведите: "Доступ разрешен" и остановите цикл,
# иначе "Извините, пользование данным ресурсом только с 18 лет" и запросите
# заново

while True:
    age = int(input("Введите ваш возраст: "))
    if age >= 18:
        print("Доступ разрешен")
        break
    else:
        print("Извините, пользование данным ресурсом только с 18 лет")